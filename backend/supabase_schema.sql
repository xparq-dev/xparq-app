-- delete_message_for_me
CREATE OR REPLACE FUNCTION delete_message_for_me(p_message_id BIGINT) RETURNS VOID AS $$ BEGIN
UPDATE public.messages
SET deleted_uids = array_append(deleted_uids, auth.uid())
WHERE id = p_message_id
  AND NOT (auth.uid() = ANY(deleted_uids));
END;
$$ LANGUAGE plpgsql;
-- get_unread_counts
CREATE OR REPLACE FUNCTION get_unread_counts(p_uid UUID) RETURNS TABLE (chat_id TEXT, unread_count BIGINT) AS $$ BEGIN RETURN QUERY
SELECT m.chat_id,
  COUNT(*) as unread_count
FROM public.messages m
  JOIN public.chats c ON m.chat_id = c.id
WHERE p_uid = ANY(c.participants)
  AND m.sender_id != p_uid
  AND m.read = false
GROUP BY m.chat_id;
END;
$$ LANGUAGE plpgsql;
-- ── TRIGGERS ─────────────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION update_last_location() RETURNS TRIGGER AS $$ BEGIN IF NEW.location_lat IS NOT NULL
  AND NEW.location_lng IS NOT NULL THEN NEW.last_location = ST_SetSRID(
    ST_MakePoint(NEW.location_lng, NEW.location_lat),
    4326
  )::geography;
END IF;
RETURN NEW;
END;
$$ LANGUAGE plpgsql;
CREATE TRIGGER tr_update_last_location BEFORE
INSERT
  OR
UPDATE OF location_lat,
  location_lng ON public.profiles FOR EACH ROW EXECUTE FUNCTION update_last_location();



