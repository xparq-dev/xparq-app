-- send_chat_message (legacy and v2 updates mentioned but consolidating here)
CREATE OR REPLACE FUNCTION send_chat_message(
    p_chat_id TEXT,
    p_sender_id UUID,
    p_content TEXT,
    p_is_sensitive BOOLEAN,
    p_is_offline_relay BOOLEAN,
    p_is_spam BOOLEAN,
    p_plaintext_preview TEXT
  ) RETURNS VOID AS $$ BEGIN
INSERT INTO public.messages (
    chat_id,
    sender_id,
    content_encrypted,
    is_sensitive,
    is_offline_relay,
    is_spam
  )
VALUES (
    p_chat_id,
    p_sender_id,
    p_content,
    p_is_sensitive,
    p_is_offline_relay,
    p_is_spam
  );
UPDATE public.chats
SET last_message = p_plaintext_preview,
  last_sender = p_sender_id,
  last_at = NOW(),
  is_sensitive = p_is_sensitive,
  is_spam = p_is_spam
WHERE id = p_chat_id;
END;
$$ LANGUAGE plpgsql;
