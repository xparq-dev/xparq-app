-- Profiles: Publicly readable, owner can update
CREATE POLICY "Public profiles are viewable by everyone" ON public.profiles FOR
SELECT USING (TRUE);
CREATE POLICY "Users can update own profile" ON public.profiles FOR
UPDATE USING (auth.uid() = id);
CREATE POLICY "Users can insert their own profile" ON public.profiles FOR
INSERT WITH CHECK (auth.uid() = id);
-- Chats: Participants can see/create
CREATE POLICY "Users can see own chats" ON public.chats FOR
SELECT USING (auth.uid() = ANY(participants));
CREATE POLICY "Users can create chats" ON public.chats FOR
INSERT WITH CHECK (auth.uid() = ANY(participants));
CREATE POLICY "Users can update their chats" ON public.chats FOR
UPDATE USING (auth.uid() = ANY(participants));
-- Messages: Participants can see/insert
CREATE POLICY "Users can see own messages" ON public.messages FOR
SELECT USING (
    EXISTS (
      SELECT 1
      FROM public.chats
      WHERE id = messages.chat_id
        AND auth.uid() = ANY(participants)
    )
  );
CREATE POLICY "Users can send messages to their chats" ON public.messages FOR
INSERT WITH CHECK (
    sender_id = auth.uid()
    AND EXISTS (
      SELECT 1
      FROM public.chats
      WHERE id = messages.chat_id
        AND auth.uid() = ANY(participants)
    )
  );
CREATE POLICY "Users can update messages in their chats" ON public.messages FOR
UPDATE USING (
    EXISTS (
      SELECT 1
      FROM public.chats
      WHERE id = messages.chat_id
        AND auth.uid() = ANY(participants)
    )
  );
-- Chat Settings: Owner only
CREATE POLICY "Users can manage their own chat settings" ON public.chat_settings FOR ALL USING (auth.uid() = uid);
-- Orbits: Owner only
CREATE POLICY "Users can manage their own orbits" ON public.orbits FOR ALL USING (auth.uid() = follower_id);
CREATE POLICY "Anyone can view orbits" ON public.orbits FOR
SELECT USING (TRUE);
